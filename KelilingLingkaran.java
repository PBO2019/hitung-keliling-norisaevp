public class KelilingLingkaran {
    private double phi = 3.14 ;
    private  double jari ;
    private   double Keliling;

    public  void hitung(){
          this. Keliling = 2 * phi * jari;

    }
    public void Setphi (double phi){
        this.phi = phi;

    }
    public void Setjari (double jari){
        this.jari = jari;

    }
    public double getkeliling(){
        return Keliling ;
    }
}
